public class ProductionRegla {
    String terminal;
    String noTerminal;
    public ProductionRegla(String terminal,String noTerminal){
        this.terminal=terminal;
        this.noTerminal=noTerminal;
    }
    public String getTerminal(){
        return terminal;
    }
    public String getNoTerminal(){
        return noTerminal;
    }
    
}
